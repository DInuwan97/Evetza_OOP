package com.Services.model;

public class WeddingPlan {
	
	private String req_date;
	private String meal_type;
	private String costume_type;
	private String theme_type;
	private String vehicle_type;
	private String visibility;
	
	
	public String getVisibility() {
		return visibility;
	}
	public void setVisibility(String visibility) {
		this.visibility = visibility;
	}
	public String getReq_date() {
		return req_date;
	}
	public void setReq_date(String req_date) {
		this.req_date = req_date;
	}
	public String getMeal_type() {
		return meal_type;
	}
	public void setMeal_type(String meal_type) {
		this.meal_type = meal_type;
	}
	public String getCostume_type() {
		return costume_type;
	}
	public void setCostume_type(String costume_type) {
		this.costume_type = costume_type;
	}
	public String getTheme_type() {
		return theme_type;
	}
	public void setTheme_type(String theme_type) {
		this.theme_type = theme_type;
	}
	public String getVehicle_type() {
		return vehicle_type;
	}
	public void setVehicle_type(String vehicle_type) {
		this.vehicle_type = vehicle_type;
	}
	
	
	
	
	
	

}
