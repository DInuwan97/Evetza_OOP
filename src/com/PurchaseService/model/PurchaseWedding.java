package com.PurchaseService.model;

public class PurchaseWedding {
	
	private String typeWedding;
	private String weddingTypeId;
	private String amount;
	private String forwardProviders;
	private String forwardAdmin;
	private String confirmation;
	
	
	public String getTypeWedding() {
		return typeWedding;
	}
	public void setTypeWedding(String typeWedding) {
		this.typeWedding = typeWedding;
	}
	public String getWeddingTypeId() {
		return weddingTypeId;
	}
	public void setWeddingTypeId(String weddingTypeId) {
		this.weddingTypeId = weddingTypeId;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getForwardProviders() {
		return forwardProviders;
	}
	public void setForwardProviders(String forwardProviders) {
		this.forwardProviders = forwardProviders;
	}
	public String getForwardAdmin() {
		return forwardAdmin;
	}
	public void setForwardAdmin(String forwardAdmin) {
		this.forwardAdmin = forwardAdmin;
	}
	public String getConfirmation() {
		return confirmation;
	}
	public void setConfirmation(String confirmation) {
		this.confirmation = confirmation;
	}
	
	
	
	
	
	
	
	
}
